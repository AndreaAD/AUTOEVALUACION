			<p align="justify">“El Sistema Nacional de Acreditación, SNA es el conjunto de políticas, estrategias, procesos y organismos cuyo objetivo fundamental es garantizar a la sociedad que las instituciones de educación superior que hacen parte del sistema cumplen con los más altos requisitos de calidad y que realizan sus propósitos y objetivos.  (Artículo 53 de la Ley 30 de 1992). La Acreditación es un testimonio que da el Estado sobre la calidad de un programa o institución con base en un proceso previo de evaluación en el cual intervienen la institución, las comunidades académicas y el Consejo Nacional de Acreditación.”</p>
