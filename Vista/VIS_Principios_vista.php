			<h1 style="font-size:20px; color:#005425; text-shadow: -1px 0 black, 0 1px black, 1px 0 black, 0 -1px black;">PRINCIPIOS</h1>
			
			<p align="justify">La Autoevaluación y Acreditación en la Universidad de Cundinamarca, estarán regidas por los principios enunciados en el Estatuto General (Acuerdo No. 010 de junio 20 de 2002); principios que constituyen las normas rectoras para la aplicación de todas las disposiciones de la Universidad. De ellos se destacan particularmente:</p>

			<ul>
				<li>Responsabilidad Social</li>
				<li>Universalidad</li>
				<li>Pertinencia</li>
				<li>Autonomía</li>
				<li>Excelencia Académica</li>
				<li>Equidad</li>
				<li>Transparencia</li>
			</ul>
			
			<p align="justify">Constituyen además, principios que orientan la Autoevaluación y Acreditación en la Universidad de Cundinamarca, los siguientes:</p>
			<p align="justify">Cultura de Evaluación: Resultado de una construcción social, a partir del consenso y negociación de los actores internos y externos que responda a los intereses propios y refleje las características de la sociedad que desea para hoy y se proyecta para el futuro.</p>
			<p align="justify">Ética: La Institución establece procesos de evaluación que garanticen la incorporación del Ideario y Compromisos Éticos de la Universidad de Cundinamarca.</p>
			<p align="justify">Fe pública de los resultados: Como un compromiso inherente a la responsabilidad social que atañe a la Universidad de Cundinamarca, en consecuencia divulgará oportunamente los resultados obtenidos en los procesos evaluativos</p>



