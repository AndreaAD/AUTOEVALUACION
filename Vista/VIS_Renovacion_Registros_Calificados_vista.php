			<p align="justify">La renovación del registro calificado debe ser solicitada por las instituciones de educación superior con no menos de diez (10) meses de anticipación a la fecha de vencimiento del respectivo registro.</p>
			<p align="justify">En razón a ello y atendiendo el procedimiento institucional, los Programas Académicos deberán hacer entrega del Documento Maestro y sus correspondientes soportes/evidencias con 14 meses de antelación a la fecha establecida para iniciar el proceso de solicitud de Renovación ante el Ministerio de Educación.</p>
